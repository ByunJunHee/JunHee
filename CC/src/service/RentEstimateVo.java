package service;

public class RentEstimateVo {
	private int reIdx;
	private String reName;
	private String rePhone;
	private String reAddr;
	private String reInterest;
	private String reDivision;
	public int getReIdx() {
		return reIdx;
	}
	public void setReIdx(int reIdx) {
		this.reIdx = reIdx;
	}
	public String getReName() {
		return reName;
	}
	public void setReName(String reName) {
		this.reName = reName;
	}
	public String getRePhone() {
		return rePhone;
	}
	public void setRePhone(String rePhone) {
		this.rePhone = rePhone;
	}
	public String getReAddr() {
		return reAddr;
	}
	public void setReAddr(String reAddr) {
		this.reAddr = reAddr;
	}
	public String getReInterest() {
		return reInterest;
	}
	public void setReInterest(String reInterest) {
		this.reInterest = reInterest;
	}
	public String getReDivision() {
		return reDivision;
	}
	public void setReDivision(String reDivision) {
		this.reDivision = reDivision;
	}
	
}
